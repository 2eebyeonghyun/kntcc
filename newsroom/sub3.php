                <p class="adj_t">니코틴 대체요법(Nicotine Replacement Therapy)은 담배에 다른 유해 화학성분들을 배제하고 니코틴을 패치, 껌, 사탕(Lozenge) 등의 형태로 피부, 구강, 점막 등의 형태로 외부에서 공급해주는 금연보조요법입니다. 흡연자가 흡연을 포기하지 못하는 원인의 70~90%는 금단증상과 이로인한 흡연욕구인데, 니코틴 대체요법은 이러한 증상을 감소시키고, 이를 극복할 수 있도록 도와줍니다. 하지만 금연보조제로서 니코틴 대체요법 이외에 금연침이나 약초담배 등의 경우 금연에 도움을 준다는 효과는 아직 충분하게 입증되지 않았습니다. 니코틴 보조제는 제품설명서에 명기된 사용방법이나 금기사항을 숙지하고 필요한 경우 의사와의 상담을 통해 사용방법 및 용량 등을 조절하도록 합니다. 니코틴 대체요법 제품(패치, 껌, 사탕)은 안전하게 보관하고 폐기해야 하며, 특히 영유아 및 어린이와 반려동물에게 닿지 않도록 안전하게 처리해야 합니다.</p>

